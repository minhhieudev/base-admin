<template>
  <div class="">
    <el-card>
      <div class="d-flex justify-content-between my-2">
        <div class="d-flex field-box">
          <div>
            <label>Tên</label>
            <el-input v-model="filter.name" />
          </div>
          <div class="ml-2">
            <label>Slug</label>
            <el-input v-model="filter.slug" />
          </div>
          <div class="ml-2 align-items-end d-flex">
            <el-button @click="loadData" type="primary">Tìm kiếm</el-button>
          </div>
        </div>
        <div>
          <el-button @click="goToAddNewPage()" type="primary" size="small">
            Tạo mới
          </el-button>
        </div>
      </div>
      <el-table
        :data="tableData"
        style="width: 100%">
        <el-table-column prop="name" label="Tên sản phẩm"></el-table-column>
        <el-table-column prop="sku" label="Mã sản phẩm"></el-table-column>
        <el-table-column prop="stock" label="Số lượng"></el-table-column>
        <el-table-column prop="price.regular" label="Giá bán">
          <template slot-scope="scope">
            <span> {{ formatPrice(scope.row.price.regular) }}</span>
          </template>
        </el-table-column>
        <el-table-column prop="slug" label="Đường dẫn"></el-table-column>
        <el-table-column :fixed="$isMobile?false:'right'" label="Thao tác" width="150">
          <template slot-scope="scope">
            <el-button @click.prevent="gotoDetail(scope.row)" type="success" size="mini">
              Xem
            </el-button>
            <el-button @click.prevent="confirmDelete(scope.row)" type="danger" size="mini">
              Xóa
            </el-button>
          </template>
        </el-table-column>
      </el-table>
      <div class="mt-2">
        <el-pagination
          background layout="jumper, prev, pager, next, sizes, total" :page-sizes="[25, 50, 100]" :pager-count="5"
          :page-size.sync="pagination.page_size" :total="pagination.count" :current-page.sync="pagination.current_page"
          @current-change="loadData" @size-change="handlePageSizeChange"
        />
      </div>
    </el-card>
  </div>
</template>

<script>
const ModelCode = 'product';
import { getCollection, handleDelete } from '@/api/product';
export default {
  data() {
    return {
      tableData: [],
      filter: {},
      pagination: {
        current_page: 1,
        page_size: 25
      },
      totalData: 0
    };
  },
  created () {
    this.loadData()
  },
  methods: {
    /** some handle methods */
    /** default methods */
    goToAddNewPage() {
      this.$router.push({ name: `${ModelCode}_new` })
    },
    gotoDetail(row) {
      this.$router.push({ name: `${ModelCode}_edit`, params: { id: row._id } })
    },
    confirmDelete(row) {
      this.$confirm(`Xác nhận xóa ${ModelCode} "${row.title}"?`, 'Warning', {
        confirmButtonText: 'Delete',
        type: 'warning'
      }).then(() => {
        handleDelete(row._id).then(({data}) => {
          if (data.code === 'success') {
            this.loadData()
          }
        }).finally(() => {
          this.$wrLoading(false)
        })
      }).catch()
    },
    loadData() {
      let _filter = {}
      if (this.filter.name) {
        this.filter.name = this.filter.name.trim()
        _filter.name = { $regex: this.filter.name, $options: "i" }
      }

      if (this.filter.slug) {
        this.filter.slug = this.filter.slug.trim()
        _filter.slug = this.filter.slug
      }

      getCollection({ pagination: this.pagination, filter: _filter }).then(({data}) => {
        if (data.code == 'success') {
          this.tableData = data.data

          if (data.total) {
            this.pagination.count = data.total
          }
        }
      })
    },
    handlePageSizeChange() {
      this.pagination.current_page = 1
      this.loadData()
    }
  }
}
</script>

